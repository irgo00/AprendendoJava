    private class ComboBoxHandler implements ItemListener {

        @Override
        public void itemStateChanged(ItemEvent e) {
            Questao questao = (Questao) jComboBoxEscolhaQuestao.getSelectedItem();
            controle.mudarQuestaoAtual(questao);
            atualizarQuestaoTela();            
        }
    }

    private class CheckBoxHandler implements ItemListener {

        private boolean mudarSoma;

        public CheckBoxHandler() {
            this.mudarSoma = true;
        }

        public void setMudar(boolean mudar) {
            this.mudarSoma = mudar;
        }

        @Override
        public void itemStateChanged(ItemEvent e) {
            int somaAtual = 0;
            if (this.mudarSoma) {
                if (jCheckBoxOp01.isSelected()){
                    somaAtual += 1;
                }
                if (jCheckBoxOp02.isSelected()){
                    somaAtual += 2;
                }
                if (jCheckBoxOp04.isSelected()){
                    somaAtual += 4;
                }
                if (jCheckBoxOp08.isSelected()){
                    somaAtual += 8;
                }
                controle.registrarResposta(somaAtual);
                atualizarQuestaoTela();
            }
        }

    }

