    private double corrigirQuestao() {

        EstadoResposta estado[] = controle.obterEstadoResposta();
        for (int i = 0; i < estado.length; i++){
            if (estado[i] == EstadoResposta.MARCOU_CERTO){
                checkBoxes[i].setForeground(Color.BLUE);
            }else if (estado[i] == EstadoResposta.MARCOU_ERRADO){
                checkBoxes[i].setForeground(Color.red);
            }else{
                checkBoxes[i].setForeground(Color.BLACK);
            }
        }
        return controle.corrigirQuestao();

    }

    public void atualizarQuestaoTela() {
        jLabelDisciplina.setText(
                controle.getQuestaoAtual().getDisciplina().getNome().toUpperCase());
        jLabelNumeroQuestao.setText("QUESTÃO " + controle.getQuestaoAtual().getNumero());
        jTextAreaTextoQuestao.setText(
                controle.getQuestaoAtual().getTextoIntrodutorio() + "\n"
                + controle.getQuestaoAtual().getEnunciado());

        jCheckBoxOp01.setText(getHTML("01) " + controle.getQuestaoAtual().getAlternativas()[0].getTexto()));
        jCheckBoxOp02.setText(getHTML("02) " + controle.getQuestaoAtual().getAlternativas()[1].getTexto()));
        jCheckBoxOp04.setText(getHTML("04) " + controle.getQuestaoAtual().getAlternativas()[2].getTexto()));
        jCheckBoxOp08.setText(getHTML("08) " + controle.getQuestaoAtual().getAlternativas()[3].getTexto()));
        if (controle.getQuestaoAtual().getFigura().equals("")) {
            jButtonImagemQuestao.setEnabled(false);
        } else {
            jButtonImagemQuestao.setEnabled(true);
        }

        //aplicar check nas caixas de seleção que resultam no somatorio
        boxHandler.setMudar(false);
        boolean arrayGabarito[] = controle.obterArrayMarcacao(controle.getRespostaAtual().getSoma());
        for (int i = 0; i < arrayGabarito.length; i++) {
            checkBoxes[i].setSelected(arrayGabarito[i]);
        }
        boxHandler.setMudar(true);
        jTextFieldSomatorio.setText(String.valueOf(controle.getRespostaAtual().getSoma()));

    }

    private String getHTML(String input) {
        StringBuilder sb = new StringBuilder();
        sb.append("<html>");
        sb.append(input);
        sb.append("</html>");
        return sb.toString();
    }

